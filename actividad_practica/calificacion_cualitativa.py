calificacion=int(input("¿Cual es tu calificación?: "))

if calificacion >=0 and calificacion <=59:
    print("REPROBADO")
elif calificacion >=60 and calificacion <=70:
    print ("Regular")
elif calificacion >=71 and calificacion <= 75:
    print ("Bueno")
elif calificacion >=76 and calificacion <= 90:
    print ("Muy bueno")
elif calificacion >=91 and calificacion <= 99:
    print ("Excelente")
    